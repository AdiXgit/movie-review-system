// SearchBar.js
import React from 'react';

const SearchBar = ({ query, setQuery, handleSearch }) => {
  return (
    <div>
      <input
        type="text"
        placeholder="Search for a movie..."
        value={query}
        onChange={(e) => setQuery(e.target.value)}
      />
      <button onClick={handleSearch}>Search</button>
    </div>
  );
};

export default SearchBar;
